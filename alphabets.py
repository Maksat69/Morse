    def morse_generator(self, word):
        morse_one = ''
        words = word.replace(' ', '_').upper()
        for letter in words:
            if letter == '_':
                a = " " + '`' + " "
                morse_one += a
            if letter in self.to_mose:
                b = self.to_mose[letter] + ' '
                morse_one += b
        return morse_one

    def morse_decoder(self, word):
        morse_one = ''
        words = word.split()
        for letter in words:
            if letter in self.from_mose:
                b = self.from_mose[letter]
                morse_one += b
        return morse_one.lower()
